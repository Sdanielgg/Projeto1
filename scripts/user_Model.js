class user{
    nome="";
    idade=0;
    sexo="";
    localidade="";
    email="";
    username="";
    password="";
    constructor(nome,idade,sexo,localidade,email,username,password){
        this.nome=nome;
        this.idade=idade;
        this.sexo=sexo;
        this.localidade=localidade;
        this.email=email;
        this.username=username;
        this.password=password;
    }
}